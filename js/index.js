var Cores = [];
Cores["CLARO"] = "red";//Define Cor para Cada Operadora
Cores["VIVO"] = "blue";
Cores["TIM"] = "green";
Cores["OI"] = "Aqua";
Cores["NEXTEL"] = "GoldenRod";

	var map;
	var zoom = 13;
	function initMap() {//Funcao Para Iniciar o mapa
        var PocisaoInicial = {lat: -21.792836111111, lng: -48.1716138888888};//cria uma pociçao incial para o mapa
        if(!map){
			map = new google.maps.Map(document.getElementById('map'), {//Cria o Mapa na Div id map
			zoom: zoom,//Define o nivel de zoom do Mapa
			center: PocisaoInicial
			});
		}
		map.addListener('zoom_changed', function() {
			if(categoria == 0){
				if(this.zoom > zoom){
					MarkersZoomIN();
					zoom += 1;
				}
				else if(this.zoom < zoom){
					MarkersZoomOUT();
					zoom -= 1;
				}
			}
		});
		for(i = 0; i < Torre.length; i++){//Faz um loop na Array de Torres Definidas.
			if(document.getElementById(Torre[i].prestadora).checked){//Se Foi selecionado para a Operadora Aparecer
				if(categoria == 0){//Se Categoria de Intensidade
					var CircleSize = 7.50 + zoom - 13;
					Icon = getCircle(CircleSize,Torre[i].prestadora);
				}else{//Senao Apenas Imagem da Pocisao
					Icon = "./img/" + Torre[i].prestadora + ".png";
				}
				var PocisaoMarker = {lat: Torre[i].latitudeStel, lng: Torre[i].longitudeStel};//Pega a Pocisao do Marker da Torre
				var marker = new google.maps.Marker({//Cria o Marker ou Circulo se For o caso
					position: PocisaoMarker,
					icon: Icon,
					map: map
				});
				
				var InfoWin = '<div style="width:60px">';//Inicia Criação das Informaçoes da torre
						
				if (Torre[i].tecnologia2g) {//se tecnologia 2g, adiciona a imagem 2g as informações
					InfoWin += '<img style="height:15px;width:15px" src="img/2g.png"/> '
				}
				if (Torre[i].tecnologia3g) {//3g
					InfoWin += '<img style="height:15px;width:15px" src="img/3g.png"/> '
				}
				if (Torre[i].tecnologia4g) {//4g
					InfoWin += '<img style="height:15px;width:15px" src="img/4g.png"/> '
				}
				InfoWin += "</div>";//finaliza a Div
				var gmapInfoWin = new google.maps.InfoWindow({//Cria InfoWindow do Gmaps
					content: InfoWin
				});
				marker.set("infoWindow", gmapInfoWin);//Seta o Marker com a InfoWindow
				marker.set("indice", i);//Especifica o Indice da Array de torres ao marker, Caso Necessario
				Torre[i].marker = marker;//Especifica na Array de Torres o Marker Criado.
				Torre[i].infoWindow = gmapInfoWin;//Especifica na Array de Torres o InfoWindow Criado
				google.maps.event.addListener(marker, "mousedown", function() {//Adiciona CallBack para quando clicar no Marker
                this.get("infoWindow").open(map, this);
				})
			}
		}
        
    }
	
	function MarkersZoomIN(){
		for(i = 0; i < Torre.length; i++){
			if(Torre[i].marker){
				var MarkerIcon = Torre[i].marker.getIcon();
				MarkerIcon.scale = MarkerIcon.scale * 2;
				Torre[i].marker.setIcon(MarkerIcon);						
			}
		}
	}
	
	function MarkersZoomOUT(){
		for(i = 0; i < Torre.length; i++){
			if(Torre[i].marker){
				var MarkerIcon = Torre[i].marker.getIcon();
				MarkerIcon.scale = MarkerIcon.scale / 2;
				Torre[i].marker.setIcon(MarkerIcon);						
			}
		}
	}	
	
    function alterCategoria(val){
      	categoria = val;
		RecarregaMap();
    }
	function Check(origin){
		if( (!origin.checked) && (origin.id != "TODAS") ){
			document.getElementById("TODAS").checked = false;
		}
		if(document.getElementById("TODAS").checked){
			document.getElementById("CLARO").checked = true;
			document.getElementById("VIVO").checked = true;
			document.getElementById("TIM").checked = true;
			document.getElementById("OI").checked = true;
			document.getElementById("NEXTEL").checked = true;
		}
	}
	  
	function getCircle(magnitude,color) {
        return {
          path: google.maps.SymbolPath.CIRCLE,
          fillColor: Cores[color],
          fillOpacity: .2,
          scale: Math.pow(2, magnitude) / 2,
          strokeColor: 'white',
          strokeWeight: .5
        };
	}
	
	function RecarregaMap(){
		for(i = 0; i < Torre.length; i++){
			if(Torre[i].marker){
				Torre[i].marker.setMap(null);	
				Torre[i].marker = null;
			}
		}
		initMap();
	}